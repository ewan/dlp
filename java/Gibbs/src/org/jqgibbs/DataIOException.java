package org.jqgibbs;

public class DataIOException extends GibbsException {

	private static final long serialVersionUID = 9115334735681875332L;

	public DataIOException() {
		super();
	}

	public DataIOException(String message) {
		super(message);
	}

	public DataIOException(Throwable cause) {
		super(cause);
	}

	public DataIOException(String message, Throwable cause) {
		super(message, cause);
	}

}
