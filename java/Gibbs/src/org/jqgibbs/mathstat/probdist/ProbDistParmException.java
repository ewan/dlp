package org.jqgibbs.mathstat.probdist;

import org.jqgibbs.GibbsException;

public class ProbDistParmException extends GibbsException {

	private static final long serialVersionUID = 952420220177101061L;

	public ProbDistParmException() {
		super();
	}

	public ProbDistParmException(String message) {
		super(message);
	}

	public ProbDistParmException(Throwable cause) {
		super(cause);
	}

	public ProbDistParmException(String message, Throwable cause) {
		super(message, cause);
	}

}
