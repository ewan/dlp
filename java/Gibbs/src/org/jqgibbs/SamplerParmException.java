package org.jqgibbs;

public class SamplerParmException extends GibbsException {

	private static final long serialVersionUID = 3470936632407160150L;

	public SamplerParmException() {
		super();
	}

	public SamplerParmException(String message) {
		super(message);
	}

	public SamplerParmException(Throwable cause) {
		super(cause);
	}

	public SamplerParmException(String message, Throwable cause) {
		super(message, cause);
	}

}
